public class ternaryOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Menggunakan ternary operator untuk menentukan nilai maksimum
        int max = (a > b) ? a : b; 

        System.out.println("Nilai maksimum adalah: " + max); //nilai yang dikeluarkan a atau 10
    }
    
}
